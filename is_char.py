with open("out.txt", "w") as f:
    f.write("case(ascii)\n")
    for char in dictionary:
        f.write("    " + str(ord(char)) + ":\n    begin\n")

        for i in range(len(dictionary[char])):
            x, y = dictionary[char][i]

            if i == 0:
                f.write(
                    "        if (curr_x == origin_x + "
                    + str(x)
                    + " && curr_y == origin_y +"
                    + str(y)
                    + ") is_char = 1;\n"
                )
            else:
                f.write(
                    "        else if (curr_x == origin_x + "
                    + str(x)
                    + " && curr_y == origin_y + "
                    + str(y)
                    + ") is_char = 1;\n"
                )
        f.write("        else is_char = 0;\n    end\n")
    f.write("    default: is_char = 0;\n")
    f.write("endcase\n")
